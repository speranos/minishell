#include "../../minishell.h"

char	*ft_pwd(int fd)
{
	char	pwd[1024];

	if(getcwd(pwd, 1024) != 0)
	{
		ft_putstr_fd(pwd, fd);
		ft_putstr_fd("\n", fd);
	}
		
	return (ft_strdup(pwd));
}
